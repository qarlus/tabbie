export function isLocaleId(value: string | undefined): value is LocaleId {
  return !!value && (LOCALES as readonly string[]).includes(value);
}

/** Translate a key; falls back to English, then the key itself. */
export function t(key: string, locale: string = "en"): string {
  const loc = isLocaleId(locale) ? locale : "en";
  return MESSAGES[loc][key] ?? MESSAGES.en[key] ?? key;
}
